// ---------------------------------------------------------------------------
// Residual bonus formula - computed at epoch completion
// ---------------------------------------------------------------------------
const TOTAL_MILESTONES = 16;
const TOTAL_UPGRADES = 23;

/**
 * Calculates the Cosmic Residue bonus earned upon completing Epoch 1.
 * @param {object} gameState - Current game state with milestone / upgrade data.
 * @returns {object} Residual bonus descriptor for the meta-progression layer.
 */
function residualBonusFormula(gameState) {
  const triggeredCount = gameState.milestones
    ? gameState.milestones.filter((m) => m.triggered).length
    : 0;

  const purchasedCount = gameState.upgrades
    ? gameState.upgrades.filter((u) => u.purchased).length
    : 0;

  const completionScore =
    (triggeredCount / TOTAL_MILESTONES) * 0.5 +
    (purchasedCount / TOTAL_UPGRADES) * 0.5;

  return {
    id: 'cosmicResidue_run' + gameState.aeonCount,
    sourceEpochId: 'epoch1',
    displayName: 'Cosmic Residue',
    bonusType: 'percentage',
    targetType: 'allRates',
    value: 0.05 + completionScore * 0.15,
    tooltipText:
      'Earned by completing The Primordial Universe. Boosts all resource generation in future epochs.',
    earnedAt: Date.now(),
  };
}

